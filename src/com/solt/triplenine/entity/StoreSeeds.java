package com.solt.triplenine.entity;

import java.time.LocalDateTime;

public class StoreSeeds {
	private LocalDateTime stseedsdate;
	private String stseedsname;
	private String stseedscategory;
	private String stseedstotal;
	private String stseedsaddress;
	private String stseedsphno;
	public String getStseedsname() {
		return stseedsname;
	}
	public void setStseedsname(String stseedsname) {
		this.stseedsname = stseedsname;
	}
	public String getStseedscategory() {
		return stseedscategory;
	}
	public void setStseedscategory(String stseedscategory) {
		this.stseedscategory = stseedscategory;
	}
	public String getStseedstotal() {
		return stseedstotal;
	}
	public void setStseedstotal(String stseedstotal) {
		this.stseedstotal = stseedstotal;
	}
	public String getStseedsaddress() {
		return stseedsaddress;
	}
	public void setStseedsaddress(String stseedsaddress) {
		this.stseedsaddress = stseedsaddress;
	}
	public String getStseedsphno() {
		return stseedsphno;
	}
	public void setStseedsphno(String stseedsphno) {
		this.stseedsphno = stseedsphno;
	}
	public LocalDateTime getStseedsdate() {
		return stseedsdate;
	}
	public void setStseedsdate(LocalDateTime stseedsdate) {
		this.stseedsdate = stseedsdate;
	}
}
